function getCoordinatesByName(nazwaPrzystanka) {
	if (!nazwaPrzystanka) {
		console.warn("Podano nieprawidłową nazwę przystanku.");
		return null;
	}

	const przystanekVal = przystankiAutobusowe.find(
		(przystanek) =>
			przystanek &&
			przystanek.nazwa &&
			przystanek.nazwa.toLowerCase() === nazwaPrzystanka.toLowerCase()
	);

	if (!przystanekVal) {
		console.warn(`Nie znaleziono przystanku: "${nazwaPrzystanka}"`);
		return null;
	}

	const [lat, lng] = przystanekVal.koordynaty
		.split(",")
		.map((coord) => parseFloat(coord.trim()));
	return [lat, lng];
}
